#include <iostream>

using namespace std;

int main(int argc, const char * argv[]) {
   
    
    int a = 20,b = 20;
    
    cout << "a   = " << a << ":  b = " << b << endl;
    cout << "a++ = " << a++ << ":  ++b = " << ++b << endl;
    cout << "a   = " << a << ":  b = " << b << endl;

    
    return 0;
}
